import numpy as np

def hex_to_rgb(hex_str: str) -> np.ndarray:
    #hex_str = hex_str.strip()
    #if hex_str[0] == '#':
    hex_str = hex_str[1:] #

    r, g, b = hex_str[:2], hex_str[2:4], hex_str[4:]
    rgb = list(int(n, base=16) for n in (r, g, b))
    return np.array(rgb)

